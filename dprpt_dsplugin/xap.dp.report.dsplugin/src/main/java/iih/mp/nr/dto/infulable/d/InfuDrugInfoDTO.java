package iih.mp.nr.dto.infulable.d;


/**
 * 输液签药品信息
 * 
 */
public class InfuDrugInfoDTO{
	
	/** 医嘱项目ID */
	private String id_orsrv;
	/** 药品名称 */
	private String name;
	/** 规格 */
	private String spec;
	/** 剂量 */
	private String quan_medu;
	/** 剂量单位 */
	private String name_measdoc;
	/** 药品备注 */
	private String des_or;
	/** 高危药品标识 */
	private String fg_highrisk;

	/** getter and setter */
	
	public String getId_orsrv(){
		return this.id_orsrv;
	}

	public void setId_orsrv(String id_orsrv){
		this.id_orsrv = id_orsrv;
	}
	
	public String getName(){
		return this.name;
	}

	public void setName(String name){
		this.name = name;
	}
	
	public String getSpec(){
		return this.spec;
	}

	public void setSpec(String spec){
		this.spec = spec;
	}
	
	public String getQuan_medu(){
		return this.quan_medu;
	}

	public void setQuan_medu(String quan_medu){
		this.quan_medu = quan_medu;
	}
	
	public String getName_measdoc(){
		return this.name_measdoc;
	}

	public void setName_measdoc(String name_measdoc){
		this.name_measdoc = name_measdoc;
	}
	
	public String getDes_or(){
		return this.des_or;
	}

	public void setDes_or(String des_or){
		this.des_or = des_or;
	}

	public String getFg_highrisk() {
		return fg_highrisk;
	}

	public void setFg_highrisk(String fg_highrisk) {
		this.fg_highrisk = fg_highrisk;
	}
	
	
}