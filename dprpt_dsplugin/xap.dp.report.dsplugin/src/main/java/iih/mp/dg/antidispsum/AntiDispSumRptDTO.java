package iih.mp.dg.antidispsum;

public class AntiDispSumRptDTO {
	
	private String adm_date;
	private String med_name;
	private String med_spec;
	private String department;
	private String physician;
	private Integer quantity;
	
	public String getAdm_date() {
		return adm_date;
	}
	
	public void setAdm_date(String adm_date) {
		this.adm_date = adm_date;
	}
	
	public String getMed_name() {
		return med_name;
	}
	public void setMed_name(String med_name) {
		this.med_name = med_name;
	}
	
	public String getMed_spec() {
		return med_spec;
	}
	
	public void setMed_spec(String med_spec) {
		this.med_spec = med_spec;
	}
	
	public String getDepartment() {
		return department;
	}
	
	public void setDepartment(String department) {
		this.department = department;
	}
	
	public String getPhysician() {
		return physician;
	}
	
	public void setPhysician(String physician) {
		this.physician = physician;
	}
	
	public Integer getQuantity() {
		return quantity;
	}
	
	public void setQuantity(Integer quantity) {
		this.quantity = quantity;
	}
	
}
