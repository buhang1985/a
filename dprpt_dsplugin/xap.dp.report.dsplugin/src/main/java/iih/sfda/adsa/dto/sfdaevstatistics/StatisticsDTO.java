package iih.sfda.adsa.dto.sfdaevstatistics;

/**
 * 概况事件统计
 * 
 */
public class StatisticsDTO {

	/** 统计类型ID */
	private String id_genre;
	/** 父类事件编码 */
	private String codeEvPar;
	/** 父类事件名称 */
	private String nameEvPar;
	/** 父类事件数量 */
	private int countEvpar;
	/** 事件编码 */
	private String codeEv;
	/** 事件名称 */
	private String nameEv;
	/** 事件数量 */
	private int countEv;
	/** 事件总数 */
	private int evTotal;

	public String getId_genre() {
		return id_genre;
	}

	public void setId_genre(String id_genre) {
		this.id_genre = id_genre;
	}

	public String getCodeEvPar() {
		return codeEvPar;
	}

	public void setCodeEvPar(String codeEvPar) {
		this.codeEvPar = codeEvPar;
	}

	public String getNameEvPar() {
		return nameEvPar;
	}

	public void setNameEvPar(String nameEvPar) {
		this.nameEvPar = nameEvPar;
	}

	public int getCountEvpar() {
		return countEvpar;
	}

	public void setCountEvpar(int countEvpar) {
		this.countEvpar = countEvpar;
	}

	public String getCodeEv() {
		return codeEv;
	}

	public void setCodeEv(String codeEv) {
		this.codeEv = codeEv;
	}

	public String getNameEv() {
		return nameEv;
	}

	public void setNameEv(String nameEv) {
		this.nameEv = nameEv;
	}

	public int getCountEv() {
		return countEv;
	}

	public void setCountEv(int countEv) {
		this.countEv = countEv;
	}

	public int getEvTotal() {
		return evTotal;
	}

	public void setEvTotal(int evTotal) {
		this.evTotal = evTotal;
	}

}