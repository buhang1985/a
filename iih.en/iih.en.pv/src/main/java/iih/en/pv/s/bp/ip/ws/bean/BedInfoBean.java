package iih.en.pv.s.bp.ip.ws.bean;

import iih.bd.webservice.BaseWsParam;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;

@XmlAccessorType(XmlAccessType.FIELD)
public class BedInfoBean extends BaseWsParam implements Serializable {

    /**
     * 
     */
    private static final long serialVersionUID = 1L;

    // 病区ID
    private String Id_dep;

    // 病区编码
    private String Code_dep;

    // 病区名称
    private String Name_dep;

    // 床位编码
    private String Code_bed;

    // 床位名称
    private String Name_bed;

    // 床位状态
    private String Status;

    public String getId_dep() {
        return Id_dep;
    }

    public void setId_dep(String id_dep) {
        Id_dep = id_dep;
    }

    public String getCode_dep() {
        return Code_dep;
    }

    public void setCode_dep(String code_dep) {
        Code_dep = code_dep;
    }

    public String getName_dep() {
        return Name_dep;
    }

    public void setName_dep(String name_dep) {
        Name_dep = name_dep;
    }

    public String getCode_bed() {
        return Code_bed;
    }

    public void setCode_bed(String code_bed) {
        Code_bed = code_bed;
    }

    public String getName_bed() {
        return Name_bed;
    }

    public void setName_bed(String name_bed) {
        Name_bed = name_bed;
    }

    public String getStatus() {
        return Status;
    }

    public void setStatus(String status) {
        Status = status;
    }

    @Override
    public String toString() {
        return "BedInfoBean [Id_dep=" + Id_dep + ", Code_dep=" + Code_dep + ", Name_dep=" + Name_dep + ", Code_bed=" + Code_bed + ", Name_bed=" + Name_bed + ", Status=" + Status + "]";
    }

}
