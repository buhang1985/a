package iih.bl.cg.s.bp.ip.backcg;

import xap.mw.coreitf.d.FDateTime;
import xap.mw.coreitf.d.FDouble;

/**
 * 计费参数
 * @author yankan
 *
 */
public class DayFeeParam{
	//主键
	private String id;
	//开始时间
	private FDateTime dt_b;
	//结束时间
	private FDateTime dt_e;
	//最后计费时间
	private FDateTime dt_chg_bill;
	//价格
	private FDouble price;
	/**
	 * 对象
	 */
	private Object realObj;
	/**
	 * 构造函数
	 */
	public DayFeeParam(){
		
	}
	/**
	 * 构造函数
	 * @param id 主键
	 * @param dt_b 开始时间
	 * @param dt_e 结束时间
	 * @param dt_chg_bill 记账时间
	 * @param price 价格
	 * @param realObj 对象
	 */
	public DayFeeParam(String id,FDateTime dt_b,FDateTime dt_e,FDateTime dt_chg_bill,FDouble price,Object realObj){
		this.id = id;
		this.dt_b = dt_b;
		this.dt_e = dt_e;
		this.dt_chg_bill = dt_chg_bill;
		this.price = price;
		this.realObj = realObj;
	}
	
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the dt_b
	 */
	public FDateTime getDt_b() {
		return dt_b;
	}
	/**
	 * @param dt_b the dt_b to set
	 */
	public void setDt_b(FDateTime dt_b) {
		this.dt_b = dt_b;
	}
	/**
	 * @return the dt_e
	 */
	public FDateTime getDt_e() {
		return dt_e;
	}
	/**
	 * @param dt_e the dt_e to set
	 */
	public void setDt_e(FDateTime dt_e) {
		this.dt_e = dt_e;
	}
	/**
	 * @return the dt_chg_bill
	 */
	public FDateTime getDt_chg_bill() {
		return dt_chg_bill;
	}
	/**
	 * @param dt_chg_bill the dt_chg_bill to set
	 */
	public void setDt_chg_bill(FDateTime dt_chg_bill) {
		this.dt_chg_bill = dt_chg_bill;
	}
	/**
	 * @return the price
	 */
	public FDouble getPrice() {
		return price;
	}
	/**
	 * @param price the price to set
	 */
	public void setPrice(FDouble price) {
		this.price = price;
	}
	/**
	 * @return the realObj
	 */
	public Object getRealObj() {
		return realObj;
	}
	/**
	 * @param realObj the realObj to set
	 */
	public void setRealObj(Object realObj) {
		this.realObj = realObj;
	}
}
