package iih.pe.papt.dto.groupsummaryreport.d;

import xap.mw.core.data.BaseDTO;
import xap.mw.sf.core.annotation.RPCField;
import xap.mw.sf.core.annotation.RPCStruct;
@RPCStruct
public class AgedisDTO extends BaseDTO {

	// 年龄段编码
	@RPCField(id = 1)
	private String code;
	// 年龄段
	@RPCField(id = 2)
	private String agesegment;
	// 年龄段计数(女)
	@RPCField(id = 3)
	private Integer woman_count;
	// 年龄段百分比(女)
	@RPCField(id = 4)
	private Double woman_per;
	// 年龄段计数(合计)
	@RPCField(id = 5)
	private Integer total;
	// 年龄段百分比(合计)
	@RPCField(id = 6)
	private Double total_per;
	// 年龄段计数(男)
	@RPCField(id = 7)
	private Integer man_count;
	// 年龄段百分比(男)
	@RPCField(id = 8)
	private Double man_per;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getAgesegment() {
		return agesegment;
	}

	public void setAgesegment(String agesegment) {
		this.agesegment = agesegment;
	}

	public Integer getWoman_count() {
		return woman_count;
	}

	public void setWoman_count(Integer woman_count) {
		this.woman_count = woman_count;
	}

	public Double getWoman_per() {
		return woman_per;
	}

	public void setWoman_per(Double woman_per) {
		this.woman_per = woman_per;
	}

	public Integer getTotal() {
		return total;
	}

	public void setTotal(Integer total) {
		this.total = total;
	}

	public Double getTotal_per() {
		return total_per;
	}

	public void setTotal_per(Double total_per) {
		this.total_per = total_per;
	}

	public Integer getMan_count() {
		return man_count;
	}

	public void setMan_count(Integer man_count) {
		this.man_count = man_count;
	}

	public Double getMan_per() {
		return man_per;
	}

	public void setMan_per(Double man_per) {
		this.man_per = man_per;
	}

}
