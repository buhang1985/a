package iih.pe.papt.dto.groupsummaryreport.d;

import xap.mw.core.data.BaseDTO;
import xap.mw.sf.core.annotation.RPCField;
import xap.mw.sf.core.annotation.RPCStruct;
@RPCStruct
public class BMIdisDTO extends BaseDTO {

	// 年龄段编码
	@RPCField(id = 1)
	private String code;
	// 年龄段
	@RPCField(id = 2)
	private String agesegment;
	// 年龄段BMI(男)
	@RPCField(id = 3)
	private Double man_groupbmi;
	// 年龄段计数(男)
	@RPCField(id = 4)
	private Integer man_count;
	// 年龄段BMI(女)
	@RPCField(id = 5)
	private Double woman_groupbmi;
	// 年龄段计数(女)
	@RPCField(id = 6)
	private Integer woman_count;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getAgesegment() {
		return agesegment;
	}

	public void setAgesegment(String agesegment) {
		this.agesegment = agesegment;
	}

	public Double getMan_groupbmi() {
		return man_groupbmi;
	}

	public void setMan_groupbmi(Double man_groupbmi) {
		this.man_groupbmi = man_groupbmi;
	}

	public Integer getMan_count() {
		return man_count;
	}

	public void setMan_count(Integer man_count) {
		this.man_count = man_count;
	}

	public Double getWoman_groupbmi() {
		return woman_groupbmi;
	}

	public void setWoman_groupbmi(Double woman_groupbmi) {
		this.woman_groupbmi = woman_groupbmi;
	}

	public Integer getWoman_count() {
		return woman_count;
	}

	public void setWoman_count(Integer woman_count) {
		this.woman_count = woman_count;
	}

}
