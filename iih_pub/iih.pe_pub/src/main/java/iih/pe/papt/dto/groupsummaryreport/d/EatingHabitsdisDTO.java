package iih.pe.papt.dto.groupsummaryreport.d;

import xap.mw.core.data.BaseDTO;
import xap.mw.sf.core.annotation.RPCField;
import xap.mw.sf.core.annotation.RPCStruct;
@RPCStruct
public class EatingHabitsdisDTO extends BaseDTO {

	// 状况描述
	@RPCField(id = 1)
	private String name;
	// 符合该状况的人数(男)
	@RPCField(id = 2)
	private Integer man;
	// 符合该状况的人数(女)
	@RPCField(id = 3)
	private Integer woman;
	// 符合该状况的人数(合计)
	@RPCField(id = 4)
	private Integer total;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getMan() {
		return man;
	}

	public void setMan(Integer man) {
		this.man = man;
	}

	public Integer getWoman() {
		return woman;
	}

	public void setWoman(Integer woman) {
		this.woman = woman;
	}

	public Integer getTotal() {
		return total;
	}

	public void setTotal(Integer total) {
		this.total = total;
	}

}
