package iih.pe.papt.dto.peaptsummarydto.d;

import xap.mw.core.data.BaseDTO;
import xap.mw.sf.core.annotation.RPCField;
import xap.mw.sf.core.annotation.RPCStruct;
@RPCStruct
public class PeAptSummaryDTO extends BaseDTO  {

	// 体检日期
	@RPCField(id = 1)
	private String dt_pe;
	// 预约计数
	@RPCField(id = 2)
	private String apt_count;
	// 体检计数
	@RPCField(id = 3)
	private String pe_count;

	public String getDt_pe() {
		return dt_pe;
	}

	public void setDt_pe(String dt_pe) {
		this.dt_pe = dt_pe;
	}

	public String getApt_count() {
		return apt_count;
	}

	public void setApt_count(String apt_count) {
		this.apt_count = apt_count;
	}

	public String getPe_count() {
		return pe_count;
	}

	public void setPe_count(String pe_count) {
		this.pe_count = pe_count;
	}

}
