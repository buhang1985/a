package iih.pe.pqn.dto.peqadto.d;

import java.util.List;
import xap.mw.sf.core.annotation.RPCField;
import xap.mw.sf.core.annotation.RPCStruct;
@RPCStruct
public class PeQaGrpDTO {

	// 体检问卷问题分组主键标识
	@RPCField(id = 1)
	private String id_peqacatlog;
	// 体检问卷问题分组父ID
	@RPCField(id = 2)
	private String id_peqacatlog_par;
	// 体检问卷问题定义id
	@RPCField(id = 3)
	private List<PeQaDefDTO> id_peqacontentdef;
	// 所属集团
	@RPCField(id = 4)
	private String id_grp;
	// 所属组织
	@RPCField(id = 5)
	private String id_org;
	// 问卷分组编码
	@RPCField(id = 6)
	private String code;
	// 问卷分组名称
	@RPCField(id = 7)
	private String name;
	// 次序
	@RPCField(id = 8)
	private Integer sortno;

	public String getId_peqacatlog() {
		return id_peqacatlog;
	}

	public void setId_peqacatlog(String id_peqacatlog) {
		this.id_peqacatlog = id_peqacatlog;
	}

	public String getId_peqacatlog_par() {
		return id_peqacatlog_par;
	}

	public void setId_peqacatlog_par(String id_peqacatlog_par) {
		this.id_peqacatlog_par = id_peqacatlog_par;
	}

	public List<PeQaDefDTO> getId_peqacontentdef() {
		return id_peqacontentdef;
	}

	public void setId_peqacontentdef(List<PeQaDefDTO> id_peqacontentdef) {
		this.id_peqacontentdef = id_peqacontentdef;
	}

	public String getId_grp() {
		return id_grp;
	}

	public void setId_grp(String id_grp) {
		this.id_grp = id_grp;
	}

	public String getId_org() {
		return id_org;
	}

	public void setId_org(String id_org) {
		this.id_org = id_org;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getSortno() {
		return sortno;
	}

	public void setSortno(Integer sortno) {
		this.sortno = sortno;
	}

}
