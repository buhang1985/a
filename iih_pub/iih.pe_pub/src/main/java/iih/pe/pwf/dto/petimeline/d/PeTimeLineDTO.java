package iih.pe.pwf.dto.petimeline.d;

import xap.mw.core.data.BaseDTO;
import xap.mw.sf.core.annotation.RPCField;
import xap.mw.sf.core.annotation.RPCStruct;
@RPCStruct
public class PeTimeLineDTO extends BaseDTO {

	// 时间
	@RPCField(id = 1)
	private String time;
	// 体检状态
	@RPCField(id = 2)
	private String pestatus;
	// 科室名称
	@RPCField(id = 3)
	private String depname;
	// 诊间名称
	@RPCField(id = 4)
	private String roomname;

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getPestatus() {
		return pestatus;
	}

	public void setPestatus(String pestatus) {
		this.pestatus = pestatus;
	}

	public String getDepname() {
		return depname;
	}

	public void setDepname(String depname) {
		this.depname = depname;
	}

	public String getRoomname() {
		return roomname;
	}

	public void setRoomname(String roomname) {
		this.roomname = roomname;
	}

}
