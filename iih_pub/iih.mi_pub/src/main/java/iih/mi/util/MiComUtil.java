package iih.mi.util;

import java.util.Arrays;

/**
 * MI工具类
 * @author LIM
 * @since 2019-09-25
 */
public class MiComUtil {
	/**
	 * 判断字段是否在数组中存在
	 * @param arr 数组
	 * @param targetValue 寻找的字符
	 * @return
	 */
	public static boolean getStrInArr(String[] arr, String targetValue) {
		  return Arrays.asList(arr).contains(targetValue);
	}
}
