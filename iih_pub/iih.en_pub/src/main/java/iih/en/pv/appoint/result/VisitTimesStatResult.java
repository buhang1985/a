package iih.en.pv.appoint.result;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

import iih.bd.webservice.BaseWsParam;

/***
 * 统计门急诊就诊人次-出参解析
 * @author zhangpp
 *
 */
@XmlRootElement(name = "data")
@XmlAccessorType(XmlAccessType.FIELD)
public class VisitTimesStatResult extends BaseWsParam implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	/**
	 * 日期
	 */
	private String date;
	/**
	 * 急诊人次
	 */
	private Integer ent_times_emg;
	/**
	 * 科室编码
	 */
	private String code_dept;
	/**
	 * 科室名称
	 */
	private String name_dept;
	/**
	 * 门诊总人次
	 */
	private Integer ent_times_op;
	/**
	 * 普通号人次
	 */
	private Integer ent_times_comm;
	/**
	 * 普通号医生出诊天数
	 */
	private Float phy_days_comm;
	/**
	 * 专家号人次
	 */
	private Integer ent_times_prof;
	/**
	 * 专家号医生出诊天数
	 */
	private Float phy_days_prof;
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public Integer getEnt_times_emg() {
		return ent_times_emg;
	}
	public void setEnt_times_emg(Integer ent_times_emg) {
		this.ent_times_emg = ent_times_emg;
	}
	public String getCode_dept() {
		return code_dept;
	}
	public void setCode_dept(String code_dept) {
		this.code_dept = code_dept;
	}
	public String getName_dept() {
		return name_dept;
	}
	public void setName_dept(String name_dept) {
		this.name_dept = name_dept;
	}
	public Integer getEnt_times_op() {
		return ent_times_op;
	}
	public void setEnt_times_op(Integer ent_times_op) {
		this.ent_times_op = ent_times_op;
	}
	public Integer getEnt_times_comm() {
		return ent_times_comm;
	}
	public void setEnt_times_comm(Integer ent_times_comm) {
		this.ent_times_comm = ent_times_comm;
	}
	public Float getPhy_days_comm() {
		return phy_days_comm;
	}
	public void setPhy_days_comm(Float phy_days_comm) {
		this.phy_days_comm = phy_days_comm;
	}
	public Integer getEnt_times_prof() {
		return ent_times_prof;
	}
	public void setEnt_times_prof(Integer ent_times_prof) {
		this.ent_times_prof = ent_times_prof;
	}
	public Float getPhy_days_prof() {
		return phy_days_prof;
	}
	public void setPhy_days_prof(Float phy_days_prof) {
		this.phy_days_prof = phy_days_prof;
	}

}
