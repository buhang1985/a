	/**
	 * 是否成组医嘱<br/>
	 * 药品医嘱服务项目中，大于1个药品的为成组医嘱
	 * @return FBoolean
	 */
	public FBoolean getFg_group() {

		FArrayList hpcpOrdSrvList = this.getHpcpOrdSrvs();
		// 非药品医嘱返回不是成组医嘱
		if (!this.getSd_srvtp().startsWith(IBdSrvTpDictCodeConst.SD_SRVTP_DRUG) || hpcpOrdSrvList == null
				|| hpcpOrdSrvList.size() == 0) {
			return FBoolean.FALSE;
		}
		int i = 0;
		for(Object obj : hpcpOrdSrvList){
			HpCpAppOrdSrvDTO ordSrv = (HpCpAppOrdSrvDTO)obj;
			if(FBoolean.TRUE.equals(ordSrv.getFg_mm())){
				i++;
				if(i>1){
					return FBoolean.TRUE;
				}
			}
		}
		
		return FBoolean.FALSE;
	}
