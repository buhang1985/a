/**
 * 医保目录对照扩展服务接口
 * 
 * @author hao_wu
 *
 */
public interface IHpSrvorcaExtCudService {

	/**
	 * 删除所有对照信息
	 * 
	 * @throws BizException
	 */
	public abstract void DeleteAll() throws BizException;
	
	
	/**
	 * 删除诊疗对照信息
	 * 
	 * @throws BizException
	 */
	public abstract void DeleteTreat() throws BizException;
	
	
	/**
	 * 删除药品对照信息
	 * 
	 * @throws BizException
	 */
	public abstract void DeleteDrug() throws BizException;
}
