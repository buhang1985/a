package iih.bd.pp.anhuisrvorca.i;

import iih.bd.pp.anhuisrvorca.d.FwssCompDTO;
import xap.mw.core.data.BizException;

/**
 * 安徽东软医保服务设施对照上报服务
 * 
 * @author hao_wu
 *
 */
public interface IFwssCompReportService {
	/**
	 * 医保服务设施对照上报
	 * 
	 * @param fwssCompDtos
	 * @return
	 * @throws BizException
	 */
	public abstract FwssCompDTO[] InsurReport(FwssCompDTO[] fwssCompDtos) throws BizException;

	/**
	 * 医保服务设施对照上报撤销
	 * 
	 * @param fwssCompDtos
	 * @return
	 * @throws BizException
	 */
	public abstract FwssCompDTO[] RevokeInsurReport(FwssCompDTO[] fwssCompDtos) throws BizException;
}
