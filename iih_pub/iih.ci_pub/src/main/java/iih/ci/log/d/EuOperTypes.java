package iih.ci.log.d;

public enum EuOperTypes {
	
	MEDOPEN("0","医疗单开立"),
	MODELOPEN("1","医嘱模版开立"),
	SIGN("2","签署"),
	DELETE("3","删除"),
	REVOKE("4","撤回"),
	PRINT("5","打印"),
	OBSOLOTE("6","作废"),
	TEMPOR("7","补临时"),
	COPY("8","复制"),
	SAVE("9","另存为模板"),
	NURFEE("10","护士补费"),
	NURCONFIRM("11","护士确认");
	
	
	private final String status;
	private final String des;
	
	EuOperTypes(String status,String des){
		this.status = status;
		this.des = des;
	}
	public String Getstatus(){
		return status;
	}
	public String Getdes(){
		return des;
	}

}
