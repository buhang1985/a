package iih.ci.mrfp.mrfpxml.d;

import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlElement;

public class RecordTarget {
    private String typeCode;
    private String contextControlCode;
    private PatientRole patientRole;
    @XmlAttribute( name ="typeCode")
    public String getTypeCode() { 
         return typeCode;
    }
    @XmlAttribute( name ="contextControlCode")
    public String getContextControlCode() { 
         return contextControlCode;
    }
    @XmlElement( name ="patientRole")
    public PatientRole getPatientRole() { 
         return patientRole;
    }
    public  void setTypeCode(String typeCode) { 
         this.typeCode = typeCode;
    }
    public  void setContextControlCode(String contextControlCode) { 
         this.contextControlCode = contextControlCode;
    }
    public  void setPatientRole(PatientRole patientRole) { 
         this.patientRole = patientRole;
    }
}
