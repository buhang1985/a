package iih.ci.mrfp.mrfpxml.d;

import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlType;

@XmlType(propOrder = {"idHealth","idInHospital","idPatient","addr","telecom","patient","providerOrganization"})
public class PatientRole {
    private String classCode;
    private Identification idHealth;
    private Identification idInHospital;
    private Identification idPatient;
    private Address addr;
    private CValue telecom;
    private Patient patient;
    private ProviderOrganization providerOrganization;
    @XmlAttribute( name ="classCode")
    public String getClassCode() { 
         return classCode;
    }
    @XmlElement( name ="id")
    public Identification getIdHealth() { 
         return idHealth;
    }
    @XmlElement( name ="id")
    public Identification getIdInHospital() { 
         return idInHospital;
    }
    @XmlElement( name ="id")
    public Identification getIdPatient() { 
         return idPatient;
    }
    @XmlElement( name ="addr")
    public Address getAddr() { 
         return addr;
    }
    @XmlElement( name ="telecom")
    public CValue getTelecom() { 
         return telecom;
    }
    @XmlElement( name ="patient")
    public Patient getPatient() { 
         return patient;
    }
    @XmlElement( name ="providerOrganization")
    public ProviderOrganization getProviderOrganization() { 
         return providerOrganization;
    }
    public  void setClassCode(String classCode) { 
         this.classCode = classCode;
    }
    public  void setIdHealth(Identification idHealth) { 
         this.idHealth = idHealth;
    }
    public  void setIdInHospital(Identification idInHospital) { 
         this.idInHospital = idInHospital;
    }
    public  void setIdPatient(Identification idPatient) { 
         this.idPatient = idPatient;
    }
    public  void setAddr(Address addr) { 
         this.addr = addr;
    }
    public  void setTelecom(CValue telecom) { 
         this.telecom = telecom;
    }
    public  void setPatient(Patient patient) { 
         this.patient = patient;
    }
    public  void setProviderOrganization(ProviderOrganization providerOrganization) { 
         this.providerOrganization = providerOrganization;
    }
}
