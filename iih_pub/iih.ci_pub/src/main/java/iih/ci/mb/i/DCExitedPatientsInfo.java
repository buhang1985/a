package iih.ci.mb.i;

import java.util.List;

import org.codehaus.jackson.annotate.JsonProperty;

public class DCExitedPatientsInfo
{
    @JsonProperty(value = "MaxLine")
    private Integer MaxLine;
    @JsonProperty(value = "ErrorMessage")
    private String ErrorMessage;
    @JsonProperty(value = "IsFail")
    private Boolean IsFail;
    @JsonProperty(value = "Patients")
    private ArrayOfDCPatientHospitalRecord Patients;

    public void setMaxLine(Integer MaxLine)
    {
        this.MaxLine = MaxLine;
    }
    public Integer getMaxLine()
    {
        return this.MaxLine;
    }
    public void setErrorMessage(String ErrorMessage)
    {
        this.ErrorMessage = ErrorMessage;
    }
    public String getErrorMessage()
    {
        return this.ErrorMessage;
    }
    public void setIsFail(Boolean IsFail)
    {
        this.IsFail = IsFail;
    }
    public Boolean getIsFail()
    {
        return this.IsFail;
    }
    public void setPatients(ArrayOfDCPatientHospitalRecord Patients)
    {
        this.Patients = Patients;
    }
    public ArrayOfDCPatientHospitalRecord getPatients()
    {
        return this.Patients;
    }
}
