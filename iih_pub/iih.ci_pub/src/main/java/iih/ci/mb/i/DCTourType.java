package iih.ci.mb.i;

import java.util.List;

import org.codehaus.jackson.annotate.JsonProperty;

public class DCTourType
{
    @JsonProperty(value = "TourTypeID")
    private String TourTypeID;
    @JsonProperty(value = "TourTypeName")
    private String TourTypeName;
    @JsonProperty(value = "ParentTypeID")
    private String ParentTypeID;
    @JsonProperty(value = "TourTypeDesc")
    private String TourTypeDesc;
    @JsonProperty(value = "SubTypes")
    private ArrayOfDCTourType SubTypes;

    public void setTourTypeID(String TourTypeID)
    {
        this.TourTypeID = TourTypeID;
    }
    public String getTourTypeID()
    {
        return this.TourTypeID;
    }
    public void setTourTypeName(String TourTypeName)
    {
        this.TourTypeName = TourTypeName;
    }
    public String getTourTypeName()
    {
        return this.TourTypeName;
    }
    public void setParentTypeID(String ParentTypeID)
    {
        this.ParentTypeID = ParentTypeID;
    }
    public String getParentTypeID()
    {
        return this.ParentTypeID;
    }
    public void setTourTypeDesc(String TourTypeDesc)
    {
        this.TourTypeDesc = TourTypeDesc;
    }
    public String getTourTypeDesc()
    {
        return this.TourTypeDesc;
    }
    public void setSubTypes(ArrayOfDCTourType SubTypes)
    {
        this.SubTypes = SubTypes;
    }
    public ArrayOfDCTourType getSubTypes()
    {
        return this.SubTypes;
    }
}
