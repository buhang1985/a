package iih.ci.type;

import java.util.ArrayList;

/**
 * 字符串列表
 * @author wangqingzhu
 *
 */
public class StringList extends ArrayList<String> {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 转化为字符串
	 * @return
	 */
	public String[] asArray() {
		return this.toArray(new String[1]);
	}
}
