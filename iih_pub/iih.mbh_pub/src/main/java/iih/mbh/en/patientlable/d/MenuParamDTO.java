package iih.mbh.en.patientlable.d;


/**
 * 菜单参数
 * 
 */
public class MenuParamDTO{
	
	/** 功能编码 */
	private String fun_code;
	/** 功能名称 */
	private String name;
	/** 参数键 */
	private String key;
	/** 参数值 */
	private String value;
	/** 参数值编码 */
	private String sd_value;

	/** getter and setter */
	
	public String getFun_code(){
		return this.fun_code;
	}

	public void setFun_code(String fun_code){
		this.fun_code = fun_code;
	}
	
	public String getName(){
		return this.name;
	}

	public void setName(String name){
		this.name = name;
	}
	
	public String getKey(){
		return this.key;
	}

	public void setKey(String key){
		this.key = key;
	}
	
	public String getValue(){
		return this.value;
	}

	public void setValue(String value){
		this.value = value;
	}
	
	public String getSd_value(){
		return this.sd_value;
	}

	public void setSd_value(String sd_value){
		this.sd_value = sd_value;
	}
}