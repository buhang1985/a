package iih.mbh.nm.depnur.d;

public class NbdDepDTO {

	// 护理单元编码
	private String code;
	// 关联科室编码
	private String code_dep;
	// 管理科室
	private String id_dep;
	// 护理单元主键
	private String id_nbd_dep;
	// 护理单元分类
	private String id_nbd_dep_ca;
	// 护理单元名称
	private String name;
	// 数据状态
	private int status;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getCode_dep() {
		return code_dep;
	}

	public void setCode_dep(String code_dep) {
		this.code_dep = code_dep;
	}

	public String getId_dep() {
		return id_dep;
	}

	public void setId_dep(String id_dep) {
		this.id_dep = id_dep;
	}

	public String getId_nbd_dep() {
		return id_nbd_dep;
	}

	public void setId_nbd_dep(String id_nbd_dep) {
		this.id_nbd_dep = id_nbd_dep;
	}

	public String getId_nbd_dep_ca() {
		return id_nbd_dep_ca;
	}

	public void setId_nbd_dep_ca(String id_nbd_dep_ca) {
		this.id_nbd_dep_ca = id_nbd_dep_ca;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

}
