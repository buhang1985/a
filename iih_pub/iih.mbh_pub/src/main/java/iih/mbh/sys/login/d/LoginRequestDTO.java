package iih.mbh.sys.login.d;

import java.util.List;
import xap.mw.sf.core.annotation.RPCField;
import xap.mw.sf.core.annotation.RPCStruct;
@RPCStruct
public class LoginRequestDTO {

	// 用户名
	@RPCField(id = 1)
	private String code_user;
	// 密码
	@RPCField(id = 2)
	private String password;
	// 设备类型
	@RPCField(id = 3)
	private int mobiletype;
	// 设备序列号
	@RPCField(id = 4)
	private String serialnum;
	// 应用编码
	@RPCField(id = 5)
	private String code_app;
	// 登录类型
	@RPCField(id = 6)
	private int logintype;
	// 获取图片类型 2-手机  3-平板
	@RPCField(id = 7)
	private int logotype;

	public String getCode_user() {
		return code_user;
	}

	public void setCode_user(String code_user) {
		this.code_user = code_user;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public int getMobiletype() {
		return mobiletype;
	}

	public void setMobiletype(int mobiletype) {
		this.mobiletype = mobiletype;
	}

	public String getSerialnum() {
		return serialnum;
	}

	public void setSerialnum(String serialnum) {
		this.serialnum = serialnum;
	}

	public String getCode_app() {
		return code_app;
	}

	public void setCode_app(String code_app) {
		this.code_app = code_app;
	}

	public int getLogintype() {
		return logintype;
	}

	public void setLogintype(int logintype) {
		this.logintype = logintype;
	}

	public int getLogotype() {
		return logotype;
	}

	public void setLogotype(int logotype) {
		this.logotype = logotype;
	}

}
