package iih.bl.cg.api.d;

/**
 * 医保划价申请信息
 * @author yankan
 *
 */
public class PriceApplyInfo {
	/**
	 * 成功返回0，失败返回1
	 */
	private String resultCode;
	/**
	 * 结果信息
	 */
	private String resultMsg;
	/**
	 * 医保划价结果
	 */
	private String insurPriceData;

	/**
	 * 获取结果编码
	 * @return the resultCode
	 */
	public String getResultCode() {
		return resultCode;
	}
	/**
	 * 设置结果编码
	 * @param resultCode the resultCode to set
	 */
	public void setResultCode(String resultCode) {
		this.resultCode = resultCode;
	}

	/**
	 * 获取结果消息
	 * @return the resultMsg
	 */
	public String getResultMsg() {
		return resultMsg;
	}
	/**
	 * 设置结果消息
	 * @param resultMsg the resultMsg to set
	 */
	public void setResultMsg(String resultMsg) {
		this.resultMsg = resultMsg;
	}
	/**
	 * 获取医保划价结果
	 * @return the insurPriceData
	 */
	public String getInsurPriceData() {
		return insurPriceData;
	}
	/**
	 * 设置医保划价结果
	 * @param insurPriceData
	 */
	public void setInsurPriceData(String insurPricingData) {
		this.insurPriceData = insurPricingData;
	}
}
