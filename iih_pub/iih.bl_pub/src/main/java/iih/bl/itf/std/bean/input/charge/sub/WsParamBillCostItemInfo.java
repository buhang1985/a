package iih.bl.itf.std.bean.input.charge.sub;

import iih.bl.itf.std.bean.input.WsParamInfoWithCustom;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlElementWrapper;

/**
 * 住院补退费入参--患者信息
 * 
 * @author zx
 *
 */
@XmlAccessorType(XmlAccessType.FIELD)
public class WsParamBillCostItemInfo extends WsParamInfoWithCustom {

	private static final long serialVersionUID = 1L;

	// 费用明细：
	// 服务编码
	@XmlElement(name = "chargeCode")
	private String chargeCode;
	// 物品编码
	@XmlElement(name = "mmCode")
	private String mmCode;
	// 数量
	@XmlElement(name = "quan")
	private String quan;

	// 价格
	@XmlElement(name = "price")
	private String price;

	// 条码号
	@XmlElement(name = "barCode")
	private String barCode;

	// 规格编码
	@XmlElement(name = "specMM")
	private String specMM;

	// 实际费用
	@XmlElement(name = "cgPrice")
	private String cgPrice;

	public String getChargeCode() {
		return chargeCode;
	}

	public void setChargeCode(String chargeCode) {
		this.chargeCode = chargeCode;
	}

	public String getMmCode() {
		return mmCode;
	}

	public void setMmCode(String mmCode) {
		this.mmCode = mmCode;
	}

	public String getQuan() {
		return quan;
	}

	public void setQuan(String quan) {
		this.quan = quan;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getBarCode() {
		return barCode;
	}

	public void setBarCode(String barCode) {
		this.barCode = barCode;
	}

	public String getSpecMM() {
		return specMM;
	}

	public void setSpecMM(String specMM) {
		this.specMM = specMM;
	}

	public String getCgPrice() {
		return cgPrice;
	}

	public void setCgPrice(String cgPrice) {
		this.cgPrice = cgPrice;
	}
	

}
