package iih.bl.itf.std.bean.output.charge.sub;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;

/**
 * 费用清单医嘱服务信息
 * @author ly 2018/07/20
 *
 */
@XmlAccessorType(XmlAccessType.FIELD)
public class WsResultBillSrvInfo implements Serializable {

	private static final long serialVersionUID = 1L;
	
	//项目编码
	@XmlElement(name = "srvCode")
	private String srvCode;
	
	//项目名称
	@XmlElement(name = "srvName")
	private String srvName;
	
	//项目单位
	@XmlElement(name = "unit")
	private String unit;
	
	//规格
	@XmlElement(name = "sepcification")
	private String sepcification;
	
	//项目单价
	@XmlElement(name = "price")
	private String price;
	
	//项目数量
	@XmlElement(name = "quan")
	private String quan;
	
	//账单项目编码
	@XmlElement(name = "inccacode")
	private String inccacode;
	
	//账单项目名称
	@XmlElement(name = "inccaItm")
	private String inccaItm;
	
	//小计金额
	@XmlElement(name = "amt")
	private String amt;

	public String getSrvCode() {
		return srvCode;
	}

	public void setSrvCode(String srvCode) {
		this.srvCode = srvCode;
	}

	public String getSrvName() {
		return srvName;
	}

	public void setSrvName(String srvName) {
		this.srvName = srvName;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public String getSepcification() {
		return sepcification;
	}

	public void setSepcification(String sepcification) {
		this.sepcification = sepcification;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getQuan() {
		return quan;
	}

	public void setQuan(String quan) {
		this.quan = quan;
	}

	public String getInccacode() {
		return inccacode;
	}

	public void setInccacode(String inccacode) {
		this.inccacode = inccacode;
	}

	public String getInccaItm() {
		return inccaItm;
	}

	public void setInccaItm(String inccaItm) {
		this.inccaItm = inccaItm;
	}

	public String getAmt() {
		return amt;
	}

	public void setAmt(String amt) {
		this.amt = amt;
	}
}
