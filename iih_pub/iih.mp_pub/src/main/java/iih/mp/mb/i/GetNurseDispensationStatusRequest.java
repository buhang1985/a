package iih.mp.mb.i;


import java.util.List;

import org.codehaus.jackson.annotate.JsonProperty;

public class GetNurseDispensationStatusRequest
{
    @JsonProperty(value = "PHID")
    private String PHID;
    @JsonProperty(value = "RecipID")
    private String RecipID;
    @JsonProperty(value = "nurser")
    private String nurser;
    @JsonProperty(value = "pw")
    private String pw;

    public void setPHID(String PHID)
    {
        this.PHID = PHID;
    }
    public String getPHID()
    {
        return this.PHID;
    }
    public void setRecipID(String RecipID)
    {
        this.RecipID = RecipID;
    }
    public String getRecipID()
    {
        return this.RecipID;
    }
    public void setNurser(String nurser)
    {
        this.nurser = nurser;
    }
    public String getNurser()
    {
        return this.nurser;
    }
    public void setPw(String pw)
    {
        this.pw = pw;
    }
    public String getPw()
    {
        return this.pw;
    }
}
