package iih.mp.mb.i;


import java.util.List;

import org.codehaus.jackson.annotate.JsonProperty;

public class PSMPatientDischargerRequest
{
    @JsonProperty(value = "encId")
    private String encId;
    @JsonProperty(value = "patientId")
    private String patientId;
    @JsonProperty(value = "times")
    private Integer times;
    @JsonProperty(value = "deptCode")
    private String deptCode;
    @JsonProperty(value = "wardCode")
    private String wardCode;
    @JsonProperty(value = "dischargerTime")
    private String dischargerTime;
    @JsonProperty(value = "operaCode")
    private String operaCode;

    public void setEncId(String encId)
    {
        this.encId = encId;
    }
    public String getEncId()
    {
        return this.encId;
    }
    public void setPatientId(String patientId)
    {
        this.patientId = patientId;
    }
    public String getPatientId()
    {
        return this.patientId;
    }
    public void setTimes(Integer times)
    {
        this.times = times;
    }
    public Integer getTimes()
    {
        return this.times;
    }
    public void setDeptCode(String deptCode)
    {
        this.deptCode = deptCode;
    }
    public String getDeptCode()
    {
        return this.deptCode;
    }
    public void setWardCode(String wardCode)
    {
        this.wardCode = wardCode;
    }
    public String getWardCode()
    {
        return this.wardCode;
    }
    public void setDischargerTime(String dischargerTime)
    {
        this.dischargerTime = dischargerTime;
    }
    public String getDischargerTime()
    {
        return this.dischargerTime;
    }
    public void setOperaCode(String operaCode)
    {
        this.operaCode = operaCode;
    }
    public String getOperaCode()
    {
        return this.operaCode;
    }
}
