package iih.mp.mb.i;

import java.util.List;

import org.codehaus.jackson.annotate.JsonProperty;

public class AddVitalSignRecordOfPatientHRRequest
{
    @JsonProperty(value = "patientID")
    private String patientID;
    @JsonProperty(value = "visitID")
    private String visitID;
    @JsonProperty(value = "vitalSignNode")
    private DCVitalSignNode vitalSignNode;

    public void setPatientID(String patientID)
    {
        this.patientID = patientID;
    }
    public String getPatientID()
    {
        return this.patientID;
    }
    public void setVisitID(String visitID)
    {
        this.visitID = visitID;
    }
    public String getVisitID()
    {
        return this.visitID;
    }
    public void setVitalSignNode(DCVitalSignNode vitalSignNode)
    {
        this.vitalSignNode = vitalSignNode;
    }
    public DCVitalSignNode getVitalSignNode()
    {
        return this.vitalSignNode;
    }
}
