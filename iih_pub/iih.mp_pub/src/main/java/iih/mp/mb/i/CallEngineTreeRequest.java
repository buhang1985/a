package iih.mp.mb.i;

import java.util.List;

import org.codehaus.jackson.annotate.JsonProperty;

public class CallEngineTreeRequest
{
    @JsonProperty(value = "model")
    private Integer model;
    @JsonProperty(value = "To_tree_id")
    private String To_tree_id;
    @JsonProperty(value = "f_id")
    private String f_id;
    @JsonProperty(value = "JSON")
    private String JSON;
    @JsonProperty(value = "Re_Str")
    private String Re_Str;

    public void setModel(Integer model)
    {
        this.model = model;
    }
    public Integer getModel()
    {
        return this.model;
    }
    public void setTo_tree_id(String To_tree_id)
    {
        this.To_tree_id = To_tree_id;
    }
    public String getTo_tree_id()
    {
        return this.To_tree_id;
    }
    public void setF_id(String f_id)
    {
        this.f_id = f_id;
    }
    public String getF_id()
    {
        return this.f_id;
    }
    public void setJSON(String JSON)
    {
        this.JSON = JSON;
    }
    public String getJSON()
    {
        return this.JSON;
    }
    public void setRe_Str(String Re_Str)
    {
        this.Re_Str = Re_Str;
    }
    public String getRe_Str()
    {
        return this.Re_Str;
    }
}
