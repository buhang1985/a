package iih.mp.mb.i;


import java.util.List;

import org.codehaus.jackson.annotate.JsonProperty;

public class PrintPatientOralMedicineRequest
{
    @JsonProperty(value = "patientID")
    private String patientID;
    @JsonProperty(value = "preID")
    private String preID;
    @JsonProperty(value = "strMedIds")
    private String strMedIds;

    public void setPatientID(String patientID)
    {
        this.patientID = patientID;
    }
    public String getPatientID()
    {
        return this.patientID;
    }
    public void setPreID(String preID)
    {
        this.preID = preID;
    }
    public String getPreID()
    {
        return this.preID;
    }
    public void setStrMedIds(String strMedIds)
    {
        this.strMedIds = strMedIds;
    }
    public String getStrMedIds()
    {
        return this.strMedIds;
    }
}
