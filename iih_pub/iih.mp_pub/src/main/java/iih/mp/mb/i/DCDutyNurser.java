package iih.mp.mb.i;

import java.util.List;

import org.codehaus.jackson.annotate.JsonProperty;

public class DCDutyNurser
{
    @JsonProperty(value = "NurserCode")
    private String NurserCode;
    @JsonProperty(value = "NurserName")
    private String NurserName;
    @JsonProperty(value = "Shifts")
    private String Shifts;
    @JsonProperty(value = "PatientNumber")
    private String PatientNumber;
    @JsonProperty(value = "TotalOrder")
    private String TotalOrder;
    @JsonProperty(value = "DoneOrder")
    private String DoneOrder;
    @JsonProperty(value = "Beds")
    private String Beds;

    public void setNurserCode(String NurserCode)
    {
        this.NurserCode = NurserCode;
    }
    public String getNurserCode()
    {
        return this.NurserCode;
    }
    public void setNurserName(String NurserName)
    {
        this.NurserName = NurserName;
    }
    public String getNurserName()
    {
        return this.NurserName;
    }
    public void setShifts(String Shifts)
    {
        this.Shifts = Shifts;
    }
    public String getShifts()
    {
        return this.Shifts;
    }
    public void setPatientNumber(String PatientNumber)
    {
        this.PatientNumber = PatientNumber;
    }
    public String getPatientNumber()
    {
        return this.PatientNumber;
    }
    public void setTotalOrder(String TotalOrder)
    {
        this.TotalOrder = TotalOrder;
    }
    public String getTotalOrder()
    {
        return this.TotalOrder;
    }
    public void setDoneOrder(String DoneOrder)
    {
        this.DoneOrder = DoneOrder;
    }
    public String getDoneOrder()
    {
        return this.DoneOrder;
    }
    public void setBeds(String Beds)
    {
        this.Beds = Beds;
    }
    public String getBeds()
    {
        return this.Beds;
    }
}
