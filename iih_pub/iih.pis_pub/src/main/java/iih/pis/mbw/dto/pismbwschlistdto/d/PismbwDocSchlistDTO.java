package iih.pis.mbw.dto.pismbwschlistdto.d;

import java.util.List;
import xap.mw.sf.core.annotation.RPCField;
import xap.mw.sf.core.annotation.RPCStruct;
@RPCStruct
public class PismbwDocSchlistDTO {

	// 是否被关注
	@RPCField(id = 1)
	private String isfoc;
	// 医生编码
	@RPCField(id = 2)
	private String code_doc;
	// 医生名称
	@RPCField(id = 3)
	private String name_doc;
	// 医生职称
	@RPCField(id = 4)
	private String doctortitle;
	// 医生特长
	@RPCField(id = 5)
	private String speciality;
	// 门诊排班列表
	@RPCField(id = 6)
	private List<PismbwSchlistDTO> schlist;
	// 已预约量
	@RPCField(id = 7)
	private int subcount;
	// 医生头像
	@RPCField(id = 8)
	private String docphoto;

	public String getIsfoc() {
		return isfoc;
	}

	public void setIsfoc(String isfoc) {
		this.isfoc = isfoc;
	}

	public String getCode_doc() {
		return code_doc;
	}

	public void setCode_doc(String code_doc) {
		this.code_doc = code_doc;
	}

	public String getName_doc() {
		return name_doc;
	}

	public void setName_doc(String name_doc) {
		this.name_doc = name_doc;
	}

	public String getDoctortitle() {
		return doctortitle;
	}

	public void setDoctortitle(String doctortitle) {
		this.doctortitle = doctortitle;
	}

	public String getSpeciality() {
		return speciality;
	}

	public void setSpeciality(String speciality) {
		this.speciality = speciality;
	}

	public List<PismbwSchlistDTO> getSchlist() {
		return schlist;
	}

	public void setSchlist(List<PismbwSchlistDTO> schlist) {
		this.schlist = schlist;
	}

	public int getSubcount() {
		return subcount;
	}

	public void setSubcount(int subcount) {
		this.subcount = subcount;
	}

	public String getDocphoto() {
		return docphoto;
	}

	public void setDocphoto(String docphoto) {
		this.docphoto = docphoto;
	}

}
