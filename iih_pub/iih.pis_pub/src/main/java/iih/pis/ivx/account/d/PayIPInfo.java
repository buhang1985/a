package iih.pis.ivx.account.d;

import java.io.Serializable;
import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;

@XmlAccessorType(XmlAccessType.FIELD)
public class PayIPInfo implements Serializable {
	private static final long serialVersionUID = 1L;

	@XmlElement(name = "patCode")
	private String patCode;
	
	@XmlElement(name = "patName")
	private String patName;
	
	@XmlElement(name = "patSex")
	private String patSex;
	
	@XmlElement(name = "patBorthday")
	private String patBorthday;
	
	@XmlElement(name = "sdCode")
	private String sdCode;
	
	@XmlElement(name = "IPStatus")
	private String IPStatus;
	
	@XmlElement(name = "STStatus")
	private String STStatus;
	
	@XmlElement(name = "depName")
	private String depName;
	
	@XmlElement(name = "dtIP")
	private String dtIP;
	
	@XmlElement(name = "bedNO")
	private String bedNO;
	
	@XmlElement(name = "totalAmt")
	private String totalAmt;
	
	@XmlElement(name = "prepayment")
	private String prepayment;
	
	@XmlElement(name = "balance")
	private String balance;
	
	@XmlElement(name = "note")
	private String note;

	public String getPatCode() {
		return patCode;
	}

	public void setPatCode(String patCode) {
		this.patCode = patCode;
	}

	public String getPatName() {
		return patName;
	}

	public void setPatName(String patName) {
		this.patName = patName;
	}

	public String getPatSex() {
		return patSex;
	}

	public void setPatSex(String patSex) {
		this.patSex = patSex;
	}

	public String getPatBorthday() {
		return patBorthday;
	}

	public void setPatBorthday(String patBorthday) {
		this.patBorthday = patBorthday;
	}

	public String getSdCode() {
		return sdCode;
	}

	public void setSdCode(String sdCode) {
		this.sdCode = sdCode;
	}

	public String getIPStatus() {
		return IPStatus;
	}

	public void setIPStatus(String iPStatus) {
		IPStatus = iPStatus;
	}

	public String getSTStatus() {
		return STStatus;
	}

	public void setSTStatus(String sTStatus) {
		STStatus = sTStatus;
	}

	public String getDepName() {
		return depName;
	}

	public void setDepName(String depName) {
		this.depName = depName;
	}

	public String getDtIP() {
		return dtIP;
	}

	public void setDtIP(String dtIP) {
		this.dtIP = dtIP;
	}

	public String getBedNO() {
		return bedNO;
	}

	public void setBedNO(String bedNO) {
		this.bedNO = bedNO;
	}

	public String getTotalAmt() {
		return totalAmt;
	}

	public void setTotalAmt(String totalAmt) {
		this.totalAmt = totalAmt;
	}

	public String getPrepayment() {
		return prepayment;
	}

	public void setPrepayment(String prepayment) {
		this.prepayment = prepayment;
	}

	public String getBalance() {
		return balance;
	}

	public void setBalance(String balance) {
		this.balance = balance;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}
	

}
