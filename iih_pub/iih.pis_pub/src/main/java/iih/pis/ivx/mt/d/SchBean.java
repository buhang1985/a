package iih.pis.ivx.mt.d;


import java.io.Serializable;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;
import iih.bd.webservice.BaseWsParam;

/**
 * 获取医技排班列表bean
 */
@XmlRootElement(name = "data")
@XmlAccessorType(XmlAccessType.FIELD)
public class SchBean extends BaseWsParam implements Serializable{

private static final long serialVersionUID = 1L;

	/**
	 * 星期
	 */
	private String week;
	/**
	 * 排班id
	 */
	private String idSch;
	/**
	 * 资源编码
	 */
	private String resCode;
	/**
	 * 资源名称
	 */
	private String resName;
	/**
	 * 科室编码
	 */
	private String deptCode;
	/**
	 * 科室名称
	 */
	private String deptName;
	/**
	 * 排班日期
	 */
	private String schDate;
	/**
	 * 午别
	 */
	private String dayslottp;
	/**
	 * 上下午
	 */
	private String ampm;
	/**
	 * 服务编码
	 */
	private String codeSrv;
	/**
	 * 服务名称
	 */
	private String nameSrv;
	/**
	 * 总号源数
	 */
	private String total;
	/**
	 * 可使用号源数量
	 */
	private String remain;
	/**
	 * 排班分类
	 */
	private String codeScca;
	/**
	 * 说明
	 */
	private String speciality;
	
	public String getIdSch() {
		return idSch;
	}

	public void setIdSch(String idSch) {
		this.idSch = idSch;
	}

	public String getResCode() {
		return resCode;
	}

	public void setResCode(String resCode) {
		this.resCode = resCode;
	}

	public String getResName() {
		return resName;
	}

	public void setResName(String resName) {
		this.resName = resName;
	}

	public String getDeptCode() {
		return deptCode;
	}

	public void setDeptCode(String deptCode) {
		this.deptCode = deptCode;
	}

	public String getDeptName() {
		return deptName;
	}

	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}

	public String getSchDate() {
		return schDate;
	}

	public void setSchDate(String schDate) {
		this.schDate = schDate;
	}

	public String getDayslottp() {
		return dayslottp;
	}
	
	public String getAmpm() {
		return ampm;
	}
	
	public void setAmpm(String ampm) {
		this.ampm = ampm;
	}

	public void setDayslottp(String dayslottp) {
		this.dayslottp = dayslottp;
	}

	public String getCodeSrv() {
		return codeSrv;
	}

	public void setCodeSrv(String codeSrv) {
		this.codeSrv = codeSrv;
	}

	public String getNameSrv() {
		return nameSrv;
	}

	public void setNameSrv(String nameSrv) {
		this.nameSrv = nameSrv;
	}

	public String getTotal() {
		return total;
	}

	public void setTotal(String total) {
		this.total = total;
	}

	public String getRemain() {
		return remain;
	}

	public void setRemain(String remain) {
		this.remain = remain;
	}

	public String getCodeScca() {
		return codeScca;
	}

	public void setCodeScca(String codeScca) {
		this.codeScca = codeScca;
	}

	public String getSpeciality() {
		return speciality;
	}

	public void setSpeciality(String speciality) {
		this.speciality = speciality;
	}

	public String getWeek() {
		return week;
	}

	public void setWeek(String week) {
		this.week = week;
	}
	
	
}
