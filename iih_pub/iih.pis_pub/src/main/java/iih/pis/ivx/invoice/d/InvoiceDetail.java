package iih.pis.ivx.invoice.d;

public class InvoiceDetail {

	//发票行性质 0 正常 1折扣 2 被折扣(必填)
	private String fphxz;
	
	//19位税收分类编码(必填)
	private String spbm;
	
	//项目名称(必填)
	private String xmmc;
	
	//计量单位
	private String dw;
	
	//规格型号
	private String ggxh;
	
	//项目数量(必填)
	private String xmsl;
	
	//项目单价(必填)
	private String xmdj;
	
	//项目金额 不含税，单位元 两位小数(必填)
	private String xmje;
	
	//税率 精确到两位小数 如0.01(必填)
	private String sl;
	
	//税额 单位元 两位小数(必填)
	private String se;

	public String getFphxz() {
		return fphxz;
	}

	public String getSpbm() {
		return spbm;
	}

	public String getXmmc() {
		return xmmc;
	}

	public String getDw() {
		return dw;
	}

	public String getGgxh() {
		return ggxh;
	}

	public String getXmsl() {
		return xmsl;
	}

	public String getXmdj() {
		return xmdj;
	}

	public String getXmje() {
		return xmje;
	}

	public String getSl() {
		return sl;
	}

	public String getSe() {
		return se;
	}

	public void setFphxz(String fphxz) {
		this.fphxz = fphxz;
	}

	public void setSpbm(String spbm) {
		this.spbm = spbm;
	}

	public void setXmmc(String xmmc) {
		this.xmmc = xmmc;
	}

	public void setDw(String dw) {
		this.dw = dw;
	}

	public void setGgxh(String ggxh) {
		this.ggxh = ggxh;
	}

	public void setXmsl(String xmsl) {
		this.xmsl = xmsl;
	}

	public void setXmdj(String xmdj) {
		this.xmdj = xmdj;
	}

	public void setXmje(String xmje) {
		this.xmje = xmje;
	}

	public void setSl(String sl) {
		this.sl = sl;
	}

	public void setSe(String se) {
		this.se = se;
	}
	
}
