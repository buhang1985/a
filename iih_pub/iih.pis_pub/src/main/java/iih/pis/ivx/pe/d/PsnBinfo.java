package iih.pis.ivx.pe.d;

public class PsnBinfo {
	private String psn_id;
	private String name;
	private String sex;
	private String birthdate;
	private String idcode;
	private String mob;
	private String regdate;

	public String getPsn_id() {
		return psn_id;
	}
	public void setPsn_id(String psn_id) {
		this.psn_id = psn_id;
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getBirthdate() {
		return birthdate;
	}
	public void setBirthdate(String birthdate) {
		this.birthdate = birthdate;
	}
	public String getIdcode() {
		return idcode;
	}
	public void setIdcode(String idcode) {
		this.idcode = idcode;
	}
	public String getMob() {
		return mob;
	}
	public void setMob(String mob) {
		this.mob = mob;
	}
	
	public String getRegdate() {
		return regdate;
	}
	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
	public String toString() {
		return "PsnBinfo [psn_id=" + psn_id + ", name=" + name + ", sex=" + sex
				+ ", birthdate=" + birthdate + ", idcode=" + idcode + ", mob="
				+ mob + "]";
	}
}
