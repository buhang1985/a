package iih.sc.comm;

/**
 * 帮助类
 * @author Kaptain
 *
 */
public class IntegerUtils {
	/**
	 * 是否为奇数
	 * @param i
	 * @return
	 */
	public static boolean isOdd(int i){
		 return i%2!=0;
	}
	/**
	 * 是否为偶数
	 * @param i
	 * @return
	 */
	public static boolean isEven(int i){
		return i%2==0;
	}
	/**
	 * 是否大于0
	 * @param i
	 * @return
	 */
	public static boolean isMoreThanZero(Integer i) {
		return i != null && i > 0;
	}
}
