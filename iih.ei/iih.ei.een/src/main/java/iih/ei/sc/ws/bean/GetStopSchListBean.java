package iih.ei.sc.ws.bean;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

import iih.bd.webservice.BaseWsParam;

/**
 * 获取医技停诊列表
 */
@XmlRootElement(name = "data")
@XmlAccessorType(XmlAccessType.FIELD)
public class GetStopSchListBean  extends BaseWsParam implements Serializable{

	private static final long serialVersionUID = 1L;

	/**
	 * 排班id
	 */
	private String idSch;
	/**
	 * 排班日期
	 */
	private String schDate;
	/**
	 * 科室编码
	 */
	private String deptCode;
	/**
	 * 科室名称
	 */
	private String deptName;
	/**
	 * 资源名称
	 */
	private String resName;
	/**
	 * 资源编码
	 */
	private String resCode;
	/**
	 * 服务名称
	 */
	private String srvName;
	/**
	 * 服务编码
	 */
	private String srvCode;
	/**
	 * 午别编码
	 */
	private String dayslottp;
	/**
	 * 午别名称
	 */
	private String ampm;
	/**
	 * 剩余号源数
	 */
	private Integer remain;
	/**
	 * 特长
	 */
	private String note;
	/**
	 * 开放标志  1:开放  0:不开放
	 */
	private String fgActive;
	
	public String getIdSch() {
		return idSch;
	}

	public void setIdSch(String idSch) {
		this.idSch = idSch;
	}

	public String getSchDate() {
		return schDate;
	}

	public void setSchDate(String schDate) {
		this.schDate = schDate;
	}

	public String getDeptCode() {
		return deptCode;
	}

	public void setDeptCode(String deptCode) {
		this.deptCode = deptCode;
	}

	public String getDeptName() {
		return deptName;
	}

	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}

	public String getResName() {
		return resName;
	}

	public void setResName(String resName) {
		this.resName = resName;
	}

	public String getResCode() {
		return resCode;
	}

	public void setResCode(String resCode) {
		this.resCode = resCode;
	}

	public String getSrvName() {
		return srvName;
	}

	public void setSrvName(String srvName) {
		this.srvName = srvName;
	}

	public String getSrvCode() {
		return srvCode;
	}

	public void setSrvCode(String srvCode) {
		this.srvCode = srvCode;
	}

	public String getDayslottp() {
		return dayslottp;
	}

	public void setDayslottp(String dayslottp) {
		this.dayslottp = dayslottp;
	}

	public String getAmpm() {
		return ampm;
	}

	public void setAmpm(String ampm) {
		this.ampm = ampm;
	}

	public Integer getRemain() {
		return remain;
	}

	public void setRemain(Integer remain) {
		this.remain = remain;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}

	public String getFgActive() {
		return fgActive;
	}

	public void setFgActive(String fgActive) {
		this.fgActive = fgActive;
	}

	
}
