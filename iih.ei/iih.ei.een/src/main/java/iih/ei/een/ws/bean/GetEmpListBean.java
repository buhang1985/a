package iih.ei.een.ws.bean;

import iih.bd.webservice.BaseWsParam;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "data")
@XmlAccessorType(XmlAccessType.FIELD)
public class GetEmpListBean extends BaseWsParam implements Serializable {

	private static final long serialVersionUID = 1L;

	/**
	 * 员工id
	 */
	private String id_emp = "";
	/**
	 * 编码
	 */
	private String code = "";
	/**
	 * 姓名
	 */
	private String name = "";
	/**
	 * 性别
	 */
	private String sd_sex = "";
	/**
	 * 出生日期
	 */
	private String dt_birthday = "";
	/**
	 * 科室编码
	 */
	private String code_dep = "";
	/**
	 * 科室名称
	 */
	private String name_dep = "";
	/**
	 * 登录名
	 */
	private String name_login = "";
	
	public String getId_emp() {
		return id_emp;
	}
	public void setId_emp(String id_emp) {
		this.id_emp = id_emp;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSd_sex() {
		return sd_sex;
	}
	public void setSd_sex(String sd_sex) {
		this.sd_sex = sd_sex;
	}
	public String getDt_birthday() {
		return dt_birthday;
	}
	public void setDt_birthday(String dt_birthday) {
		this.dt_birthday = dt_birthday;
	}
	public String getCode_dep() {
		return code_dep;
	}
	public void setCode_dep(String code_dep) {
		this.code_dep = code_dep;
	}
	public String getName_dep() {
		return name_dep;
	}
	public void setName_dep(String name_dep) {
		this.name_dep = name_dep;
	}
	public String getName_login() {
		return name_login;
	}
	public void setName_login(String name_login) {
		this.name_login = name_login;
	}
	
}
